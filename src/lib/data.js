  // Helper functions
  export function getCategoryById(id) {
    const category = categories.find((c) => c.id === id)
    if (category) {
      // Attach subcategories to the category
      category.subcategories = subcategories.filter((s) => s.categoryId === id)
    }
    return category
  }
  
  export function getSubcategoryById(id) {
    return subcategories.find((s) => s.id === id)
  }
  
  export function getProductById(id) {
    return products.find((p) => p.id === id)
  }
  
  export function getProductsBySubcategoryId(subcategoryId) {
    return products.filter((p) => p.subcategoryId === subcategoryId)
  }
  
  export function getProductsByCategoryId(categoryId) {
    return products.filter((p) => p.categoryId === categoryId)
  }
  
  export function getFeaturedCategories(limit = 6) {
    const featuredCategories = [...categories].slice(0, limit)
  
    // Attach subcategories to each category
    return featuredCategories.map((category) => {
      return {
        ...category,
        subcategories: subcategories.filter((s) => s.categoryId === category.id),
      }
    })
  }
  
  export function getFeaturedProducts(limit = 8) {
    return [...products].sort(() => 0.5 - Math.random()).slice(0, limit)
  }
  
  